const getCountryCodeFromName = (countryName) => {
    const country = Object.keys(countryCodes).find(
        (key) => key.toLowerCase() === countryName.toLowerCase()
    );
    return country ? countryCodes[country].code : undefined;
}

const getCountryNameFromCode = (code) => {
    const country = Object.keys(countryCodes).find((key) => {
        const codes = Array.isArray(countryCodes[key].code) ? countryCodes[key].code : [countryCodes[key].code];
        return codes.includes(code.toString());
    });
    return country || undefined;
}


const getCountryNameFromISO = (isoCode) => {
    const country = Object.keys(countryCodes).find((key) => countryCodes[key].iso === isoCode.toUpperCase());
    return country || undefined;
}

module.exports = {
    getCountryCodeFromName,
    getCountryNameFromCode,
    getCountryNameFromISO
}

